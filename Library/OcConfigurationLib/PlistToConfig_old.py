def xml_start(elem, tab, hide):
  if hide != True:
    plist_print('<', elem.tag, end='', tab=tab)

def xml_error_if_unused_attr(elem):
  if len(elem.attrib) > 0:
    error('unhandled attributes %s on <%s>' % (elem.attrib, elem.tag))

def xml_stop(elem, hide):
  xml_error_if_unused_attr(elem)
  if hide != True:
    plist_print('>')

def xml_display_attr(name, value, hide, always_show = False):
  if hide != True:
    if (always_show or flags & SHOW_ORIGINAL == 0) and value is not None:
      plist_print(' ', name, '="', value, '"', end='')

def xml_stop_then_close(elem, contents, hide, quick_close = False):
  xml_error_if_unused_attr(elem)
  if hide != True:
    if contents is not None:
      plist_print('>', contents, '</', elem.tag, '>')
    else:
      if quick_close or flags & SHOW_ORIGINAL == 0:
        plist_print('/>')
      else:
        # for when we need to non-quick-close to match original
        plist_print('></', elem.tag, '>')

def xml_open(elem, tab, hide):
  if hide != True:
    plist_print('<', elem.tag, '>', tab=tab)

def xml_close(elem, tab, hide):
  if hide != True:
    plist_print('</', elem.tag, '>', tab=tab)

def xml_open_close(elem, tab, hide, quick_close = False):
  xml_start(elem, tab, hide)
  xml_stop_then_close(elem, elem.text, hide, quick_close)

def xml_consume_attr(elem, name, hide):
  value = elem.attrib.pop(name, None)
  xml_display_attr(name, value, hide)
  return value

def check_key(parent, child, index):
  if child.schema_type != 'key':
    error('<key> required as ', 'first' if index == 0 else 'every even' , ' element of <', parent.tag, '>')

def parse_key(elem, node, tab, hide):
  if xml_consume_attr(elem, 'type', None) is not None:
    error('type attribute should be applied to data not key')
    
  if xml_consume_attr(elem, 'size', None) is not None:
    error('size attribute should be applied to data not key')

  # indicates an element which should go into the .h file, but not the .c nor the regenerated .plist
  str_remove = xml_consume_attr(elem, 'remove', None)
  remove = str_to_bool(str_remove)

  # do not allow non-hidden element to reset if already in hidden tree
  if hide != True:
    hide = remove
  
  comment = xml_consume_attr(elem, 'comment', hide)
  xml_display_attr('remove', str_remove, hide)
  xml_stop_then_close(elem, elem.text, hide)

  if node.c is None:
    node.c = elem.text

  if node.h is None:
    node.h = elem.text.upper()

  return PlistKey(value=elem.text, node=node, comment=comment, remove=remove, hide=hide, tab=tab)

def parse_boolean(elem, node, tab, path, hide):
  default = xml_consume_attr(elem, 'default', hide)
  xml_stop_then_close(elem, elem.text, hide, quick_close = True)
  return OcSchemaElement(schema_type='BOOLEAN', value=elem.tag, default=default, tab=tab, path=path, ref=hc('BOOLEAN'))

def parse_string(elem, node, tab, path, hide):
  default = xml_consume_attr(elem, 'default', hide)
  if default is not None:
    default = '"%s"' % default

  const = xml_consume_attr(elem, 'const', hide)
  if const is not None:
    if default is not None:
      error('do not specify both default and const on <string>')
    default = const

  xml_stop_then_close(elem, elem.text, hide)
  return OcSchemaElement(schema_type='STRING', value=elem.text, default=default, tab=tab, path=path, ref=hc('OC_STRING'))

def parse_pointer(elem, node, tab, path, hide):
  to = xml_consume_attr(elem, 'to', hide)
  xml_stop_then_close(elem, None, hide)

  if to is None:
    to = 'uint8'

  ref = '%s *' % to.upper()

  return OcSchemaElement(schema_type='POINTER', tab=tab, path=path, ref=hc(ref))

def parse_data(elem, node, tab, path, hide, is_integer = False):
  data_type = xml_consume_attr(elem, 'type', None)
  data_size = xml_consume_attr(elem, 'size', None)
  default = xml_consume_attr(elem, 'default', None)
  data = elem.text

  data_print = None

  if is_integer:
    if data_type is None:
      data_type = 'uint32' # use as default for integer

    if data is not None:
      if data_type == 'uint64':
        data_print = '0x%16x' % int(data)
      elif data_type == 'uint16':
        data_print = '0x%4x' % int(data)
      elif data_type == 'uint8':
        data_print = '0x%2x' % int(data)
      else:
        data_print = '0x%8x' % int(data)
  else:
    if data is not None:
      data_bytes = base64.b64decode(data)
      byte_length = len(data_bytes)
      data_print = '0x' + data_bytes.hex()

      if data_type is None or data_size is None:

        if data_type is None:
          if byte_length == 2:
            data_type = 'uint16'
          elif byte_length == 4:
            data_type = 'uint32'
          elif byte_length == 8:
            data_type = 'uint64'
          else:
            data_type = 'uint8'

        # for data of a definite length with uint8 type we allow implicit array size
        if data_type == 'uint8' and byte_length != 1 and data_size is None:
          data_size = str(byte_length)

  if data_type is None:
    if data_size is None:
      data_type = 'blob'
    else:
      data_type = 'uint8'
  elif data_type == 'blob' and data_size is not None:
    error('size attribute not valid with type="blob"')

  xml_display_attr('type', data_type, hide)
  xml_display_attr('size', data_size, hide)
  xml_display_attr('default', default, hide)
  xml_stop_then_close(elem, data if flags & SHOW_ORIGINAL != 0  else data_print, hide)

  ref = data_type.upper()
  if ref == 'BLOB':
    schema_type = 'DATA'
    ref = 'OC_DATA'
  else:
    schema_type = 'INTEGER' if is_integer else 'DATAF'

  return OcSchemaElement(schema_type=schema_type, size=data_size, value=data_print, default=default, tab=tab, path=path, ref=hc(ref))

def skip(elem, index, count, hide, tab):
  if flags & SHOW_ORIGINAL != 0:
    for i in range(index, count):
      # nuke path from here down, which supresses all emit output
      parse_elem(elem[i], tab, None, hide)
  elif hide != True:
    skip_count = count - index
    if skip_count > 0:
      plist_print('(skipping ', skip_count, ' item', '' if skip_count == 1 else 's' , ')', tab=(tab + 1))

def parse_array(elem, node, tab, path, hide, hide_children):
  comment = xml_consume_attr(elem, 'comment', hide)
  xref = xml_consume_attr(elem, 'xref', hide)

  if hide_children:
    xml_stop_then_close(elem, None, hide, True)
  else:
    xml_stop(elem, hide)

  count = len(elem)
  index = 0

  if xref is not None:
    elem_array = OcSchemaElement(schema_type='ARRAY', tab=tab, path=path, comment=comment, ref=hc(xref))
  else:
    if count == 0:
      error('No template for <array>')

    child = parse_elem(elem[index], tab, path, hide_children, context='array')
    index += 1

    key = PlistKey(node=node)
    apply_key(child, key, tab=tab)
    emit(child, 'array')

    elem_array = OcSchemaElement(schema_type='ARRAY', of=child, tab=tab, path=path, comment=comment)

  skip(elem, index, count, hide_children, tab)

  if not hide_children:
    xml_close(elem, tab, hide)

  return elem_array

def add_warnings(hide):
  if hide != True and flags & SHOW_ORIGINAL != 0:
    plist_print('\t<key>#WARNING - 1</key>')
    plist_print('\t<string>This is just a sample. Do NOT try loading it.</string>')
    plist_print('\t<key>#WARNING - 2</key>')
    plist_print('\t<string>Ensure you understand EVERY field before booting.</string>')
    plist_print('\t<key>#WARNING - 3</key>')
    plist_print('\t<string>In most cases recommended to use Sample.plist</string>')
    plist_print('\t<key>#WARNING - 4</key>')
    plist_print('\t<string>Use SampleCustom.plist only for special cases.</string>')

def init_dict(elem, path, hide, hide_children, tab, map):
  xml_display_attr('type', 'map' if map else None, hide)
  comment = xml_consume_attr(elem, 'comment', hide)
  xref = xml_consume_attr(elem, 'xref', hide)

  if hide_children:
    xml_stop_then_close(elem, None, hide, True)
  else:
    xml_stop(elem, hide)


  if path is not None and len(path) == 0:
    add_warnings(hide)

  count = len(elem)

  if count == 0 and xref is not None:
    error('No elements in <dict>')

  if count % 2 != 0:
    error('Number of nodes in <dict> must be even')

  return (count, comment, xref)

def parse_map(elem, node, tab, path, hide, hide_children):
  (count, comment, xref) = init_dict(elem, path, hide, hide_children, tab, True)

  if xref is not None:
    # no particular reason not to add it here (following the pattern of struct and array) afaik, just not required yet
    internal_error('support for xref in map not yet added')

  index = 0

  key = parse_elem(elem[index], tab, path, hide_children)
  index += 1

  check_key(elem, key, 0)

  hide_field = hide_children or key.hide

  value = parse_elem(elem[index], tab, path, hide_field, context='map')
  index += 1

  key.name = None
  apply_key(value, key, tab=tab)
  emit(value, context='map')

  skip(elem, index, count, hide_children, tab)

  if not hide_children:
    xml_close(elem, tab, hide)

  return OcSchemaElement(schema_type='MAP', of=value, tab=tab, path=path, comment=comment)

def parse_struct(elem, node, tab, path, hide, hide_children):
  (count, comment, xref) = init_dict(elem, path, hide, hide_children, tab, False)

  index = 0
  if xref is not None:
    elem_struct = OcSchemaElement(schema_type='STRUCT', tab=tab, path=path, comment=comment, ref=hc(xref))
  else:
    fields = []

    while index < count:
      key = parse_elem(elem[index], tab, path, hide_children)
      index += 1

      check_key(elem, key, index)

      # remove means .h but .not .c or .plist; hide means .h and .c but not .plist
      hide_field = hide_children or key.hide or (flags & SHOW_ORIGINAL != 0 and key.remove)

      if key.value is None and key.replace_name is None:
        error('<key> tag within <dict> fields template must have name attribute or xml content to use as variable name')

      if path is not None and len(path) == 0:
        emit_section(key)

      oc_child = parse_elem(elem[index], tab, path, hide_field, next_node=key.node, context='struct')
      index += 1

      apply_key(oc_child, key, tab=tab)
      emit(oc_child, context='struct')

      fields.append(oc_child)

    elem_struct = OcSchemaElement(schema_type='STRUCT', of=fields, tab=tab, path=path, comment=comment)

  skip(elem, index, count, hide_children, tab)
  
  if not hide_children:
    xml_close(elem, tab, hide)

  return elem_struct

def parse_plist(elem, node, tab, path, hide):
  xml_consume_attr(elem, 'version', hide)

  xml_stop(elem, hide)

  count = len(elem)
  if count != 1:
    error('Invalid contents for <plist>')

  child = parse_elem(elem[0], tab, path, hide, indent=False)

  xml_close(elem, tab, hide)

  return child

def parse_elem(elem, tab, path, hide, next_node = None, indent = True, context = None):
  if tab == None:
    tab = 0

  if indent:
    tab += 1

  context_print('CONTEXT: ', context, tab = tab)
  
  # hide elements
  hide_children = None
  hide_attr = xml_consume_attr(elem, 'hide', None)
  if hide_attr is not None:
    if hide_attr.lower() == "children":
      if not (elem.tag == 'array' or elem.tag == 'dict'):
        error('hide="children" only valid on <array> or <dict> - found on <', elem.tag, '>')
      hide_children = True
    else:
      if elem.tag != 'key':
          error('hide attr for properties should be on <key> - found on <', elem.tag, '>')

      if hide != True:
        hide = str_to_bool(hide_attr)
  hide_children = hide_children or hide

  remove_attr = xml_consume_attr(elem, 'remove', None)
  remove = str_to_bool(remove_attr)

  xml_start(elem, tab, hide or remove)

  # modify current path where name of child needs to differ from name of parent
  h_node = xml_consume_attr(elem, 'h_node', hide)
  c_node = xml_consume_attr(elem, 'c_node', hide)

  node = hc(h_node, c_node)

  if path is None:
    next_path = None
  else:
    if h_node is not None or c_node is not None:
      use_node = hc(
        next_node.h if next_node is not None and h_node is None else h_node,
        next_node.c if next_node is not None and c_node is None else c_node
        )
    else:
      use_node = next_node

    # copy even if not modifying here (children may modify, we don't want to share)
    next_path = path.copy()

    if use_node is not None:
      next_path.append(use_node) # append modifies list, returns None

  if elem.tag == 'key':
    return parse_key(elem, node, tab, hide)

  if elem.tag == 'true' or elem.tag == 'false':
    return parse_boolean(elem, node, tab, next_path, hide)

  if elem.tag == 'string':
    return parse_string(elem, node, tab, next_path, hide)

  if elem.tag == 'integer':
    return parse_data(elem, node, tab, next_path, hide, True)

  if elem.tag == 'data':
    return parse_data(elem, node, tab, next_path, hide)

  if elem.tag == 'array':
    return parse_array(elem, node, tab, next_path, hide, hide_children)

  if elem.tag == 'dict':
    if xml_consume_attr(elem, 'type', None) == 'map':
      return parse_map(elem, node, tab, next_path, hide, hide_children)
    else:
      return parse_struct(elem, node, tab, next_path, hide, hide_children)

  # fake element used to insert pointer to named type
  if elem.tag == 'pointer':
    return parse_pointer(elem, node, tab, next_path, hide)

  if elem.tag == 'plist':
    return parse_plist(elem, node, tab, next_path, hide)

  error('Unhandled tag <', elem.tag, '>')

