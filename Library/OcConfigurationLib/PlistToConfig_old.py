def str_to_bool(str_bool):
  if str_bool is None:
    bool_bool = None
  else:
    lower = str_bool.lower()
    if str_bool == "0" or lower == "false" or lower == "no":
      bool_bool = False
    elif str_bool == "1" or lower == "true" or lower == "yes":
      bool_bool = True
    else:
      error('illegal bool value="', str_bool, '"')
  return bool_bool

@dataclass
class PlistKey:
  schema_type: str
  comment: str
  remove: bool
  hide: bool
  value: str
  node: hc

  def __init__(
    self,
    comment: str = None,
    remove: bool = None,
    hide: bool = None,
    value: str = None,
    node: hc = None,
    tab: int = 0
    ):

    self.schema_type = 'key'
    self.comment = comment
    self.value = value
    self.remove = remove
    self.hide = hide
    self.node = node

    plist_schema_print('[plist:key', tab=tab, end='')
    plist_schema_attr_print('comment', comment)
    plist_schema_attr_print('value', value)
    plist_schema_attr_print('remove', remove)
    plist_schema_attr_print('hide', hide)
    plist_schema_attr_print('node', node)
    plist_schema_print(']')

@dataclass
class OcSchemaElement:
  schema_type: str
  name: str
  node: hc
  path: hc
  data_type: str
  size: str
  comment: str
  value: str
  remove: bool
  default: str
  of: object
  # .h and .c file reference names, once emitted
  ref: hc

  def __init__(
    self,
    schema_type: str,
    name: str = None,
    path: hc = None,
    size: str = None,
    comment: str = None,
    value: str = None,
    remove: bool = None,
    default: str = None,
    of: object = None,
    ref: hc = None,
    tab: int = 0
    ):

    self.schema_type = schema_type
    self.name = name
    self.path = path
    self.size = size
    self.comment = comment
    self.value = value
    self.remove = remove
    self.default = default
    self.of = of
    self.ref = ref

    oc_schema_print('[OC:', schema_type, tab=tab, end='')
    oc_schema_attr_print('name', name)
    oc_schema_attr_print('path', path)
    oc_schema_attr_print('size', size)
    oc_schema_attr_print('comment', comment)
    oc_schema_attr_print('value', value)
    oc_schema_attr_print('remove', remove)
    oc_schema_attr_print('default', default)
    of_type = None
    if of is not None:
      if type(of) is list:
        of_type = 'list[%d]' % len(of)
      else:
        of_type = of.schema_type
    oc_schema_attr_print('of', of_type)
    oc_schema_attr_print('ref', ref)
    oc_schema_print(']')

def apply_key(elem, key, tab = 0):
  if elem.name is not None:
    internal_error('name should not get set more than once on OcSchemaElement')

  elem.name = key.value
  oc_schema_print('[OC: ... name="', elem.name, '"', tab=(tab+1), end='')

  if key.node is None:
    internal_error('key.node should be present at apply_key')
  elem.node = key.node
  oc_schema_print(' node=', elem.node, end='')

  elem.remove = key.remove
  if elem.remove is not None:
    oc_schema_print(' remove=', elem.remove, end='')

  oc_schema_print(']')

def emit(elem, context):
  if elem.schema_type == 'STRUCT':
    emit_struct(elem, context)
  elif elem.schema_type == 'ARRAY':
    emit_array(elem, context)
  elif elem.schema_type == 'MAP':
    emit_map(elem, context)
  elif context == 'array' and elem.schema_type == 'STRING':
    pass

